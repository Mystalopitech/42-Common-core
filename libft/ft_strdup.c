#include "libft.h"

char	*ft_strdup(const char *src)
{
	size_t			i;
	char			*dest;

	i = 0;
	dest = malloc(sizeof (char) * ft_strlen(src) + 1);
	if (!dest)
		return (dest);
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
