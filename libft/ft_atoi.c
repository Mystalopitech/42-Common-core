#include "libft.h"

int	ft_atoi(const char *str)
{
	int	sign;
	int	i;
	int	rsl;

	sign = 1;
	i = 0;
	rsl = 0;
	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == ' '))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		rsl = rsl * 10 + (str[i] - '0');
		i++;
	}
	return (rsl * sign);
}
