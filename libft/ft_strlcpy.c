#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t			slen;

	slen = ft_strlen(src);
	if (slen + 1 < size)
		ft_memcpy(dest, src, slen + 1);
	else if (size != 0)
	{
		ft_memcpy(dest, src, size - 1);
		dest[size - 1] = 0;
	}
	return (slen);
}
