#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t			source_len;

	source_len = ft_strlen(src);
	if (source_len + 1 < size)
		ft_memcpy(dest, src, source_len + 1);
	else if (size != 0)
	{
		ft_memcpy(dest, src, size - 1);
		dest[size - 1] = 0;
	}
	return (source_len);
}
